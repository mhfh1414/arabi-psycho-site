# ملف التوصيات - recommend.py
# هذا الملف يحتوي دوال مساعدة لإعطاء توصيات بناءً على نتائج الاختبارات

def recommend_based_on_psych(score: int) -> str:
    """
    يعطي توصية بناءً على نتيجة الاختبار النفسي
    """
    if score >= 80:
        return "توصية: حالتك ممتازة، استمر في أسلوب حياتك الصحي."
    elif score >= 50:
        return "توصية: حالتك متوسطة، حاول زيادة ممارسة الرياضة وتقليل التوتر."
    else:
        return "توصية: يُفضل مراجعة أخصائي نفسي لمتابعة حالتك بشكل أدق."


def recommend_based_on_personality(result: str) -> str:
    """
    يعطي توصية بناءً على نتيجة اختبار الشخصية
    """
    if result == "انطوائي":
        return "توصية: جرب التفاعل أكثر مع الآخرين لتنمية مهاراتك الاجتماعية."
    elif result == "انبساطي":
        return "توصية: استثمر طاقتك في بناء علاقات قوية ومثمرة."
    else:
        return "توصية: اعمل على موازنة حياتك بين العمل والراحة."
