# وحدة توصيات مبدئية (تجريبية)

def recommend_by_score(score: int) -> str:
    """
    يعيد توصية نصية حسب الدرجة المدخلة.
    :param score: رقم صحيح يمثل مجموع النقاط
    :return: نص توصية
    """
    if score <= 2:
        return "يبدو أنك بخير، استمر في الاهتمام بنفسك."
    elif score <= 5:
        return "ننصحك بمتابعة أنشطة الاسترخاء وتقنيات التنفس."
    else:
        return "يفضل استشارة مختص نفسي لمزيد من التقييم."

def recommend_info() -> str:
    """يعيد وصف قصير عن وحدة التوصيات."""
    return "وحدة التوصيات (recommend) جاهزة للعمل."
